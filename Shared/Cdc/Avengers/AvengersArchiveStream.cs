        private readonly Stream _inner;
        private readonly byte[] _key;
        private bool _scrambleKey;
        private readonly byte[] _state = new byte[0x100];

        private readonly int _archivePartIdx;
        private long _pos;
        private long _blockPos = -1;
        private readonly byte[] _block = new byte[0x800];

        public AvengersArchiveStream(int archiveId, int archivePartIdx, Stream inner)
        {
            _archivePartIdx = archivePartIdx;
            _inner = inner;

            if (archiveId == 10)
            {
                _key = Update1Key;
                _scrambleKey = true;
            }
            else
            {
                _key = DefaultKey;
                _scrambleKey = false;
            }
        }

        public override bool CanRead => true;

        public override bool CanSeek => true;

        public override bool CanWrite => false;

        public override long Length => _inner.Length;

        public override long Position
        {
            get => _pos;
            set => Seek(value, SeekOrigin.Begin);
        }

        public override void Flush()
        {
        }

        public override int Read(byte[] buffer, int offset, int count)
        {
            int remaining = count;
            while (remaining > 0)
            {
                LoadBlock();
                int offsetInBlock = (int)(_pos % 0x800);
                int chunkSize = Math.Min(remaining, 0x800 - offsetInBlock);
                Array.Copy(_block, offsetInBlock, buffer, offset, chunkSize);
                _pos += chunkSize;
                offset += chunkSize;
                remaining -= chunkSize;
            }
            return count;
        }

        public override long Seek(long offset, SeekOrigin origin)
        {
            _pos = _inner.Seek(offset, origin);
            return _pos;
        }

        public override void SetLength(long value)
        {
            throw new NotSupportedException();
        }

        public override void Write(byte[] buffer, int offset, int count)
        {
            throw new NotSupportedException();
        }

        private void LoadBlock()
        {
            long blockPos = _pos & ~0x7FF;
            if (blockPos == _blockPos)
                return;

            _blockPos = blockPos;
            _inner.Position = blockPos;
            _inner.Read(_block, 0, _block.Length);

            Array.Copy(_key, _state, 0x100);
            int sequentialIdx = CalculateInitialKeyByteIndex(blockPos | ((long)_archivePartIdx << 32));

            if (_scrambleKey)
            {
                int idx0 = KeyScrambleTable[sequentialIdx ^ 89];
                int idx1 = KeyScrambleTable[(byte)((sequentialIdx ^ 89) + 1)];
                byte scrambledState0 = KeyScrambleTable[_state[idx0]];
                _state[idx0] = KeyScrambleTable[_state[idx1]];
                _state[idx1] = KeyScrambleTable[scrambledState0];

                int scrambleIdx = (byte)(_key[29] + 1 + (sequentialIdx ^ 89) + 1);
                int idx2 = KeyScrambleTable[scrambleIdx];
                int idx3 = KeyScrambleTable[(byte)(scrambleIdx + 1)];
                byte scrambledState2 = KeyScrambleTable[_state[idx2]];
                _state[idx2] = KeyScrambleTable[_state[idx3]];
                _state[idx3] = KeyScrambleTable[scrambledState2];
            }

            byte prevInput = 0;
            for (int i = 0; i < _block.Length; i++)
            {
                byte b = _block[i];
                _block[i] = (byte)(b ^ _state[prevInput] ^ _state[(byte)sequentialIdx]);
                _state[(byte)(prevInput + sequentialIdx)] = b;
                sequentialIdx++;
                prevInput = b;
            }
        }

        private static int CalculateInitialKeyByteIndex(long offset)
        {
            byte result = 8;
            for (int i = 0; i < 8; i++)
            {
                byte offsetByte = (byte)(offset >> (8 * i));
                result = InitialKeyOffsetCalcTable[offsetByte ^ result];
            }
            return result;
        }

        protected override void Dispose(bool disposing)
        {
            base.Dispose(disposing);
            _inner.Dispose();
        }
